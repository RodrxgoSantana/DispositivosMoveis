const ShowDetalhes = ({display,toogleModal,mensagem,outroParamentro}) => (   
    <Modal
          animationType="slide"
          transparent={true}
          visible={display}
          onRequestClose={toogleModal}
    >

        <View style={styles.centeredView}>
          <View style={styles.modalView}>
                <Pressable onPress={toogleModal}>
                  <Text>{mensagem + outroParamentro}</Text>
                </Pressable>
          </View>
        </View>
    
    </Modal>
        
 )

const Comentario = ({title,body}) => {

    var exemplo = 'xpto'
    
    //state para controle do Modal
    const [modal,setModal] = React.useState(false)

    function mudaModal(){
      setModal(!modal)
    }

    return(
    <View>
      <ShowDetalhes display={modal} toogleModal={mudaModal} mensagem={body} outroParamentro={exemplo}/>
      
      <Pressable onPress={mudaModal}>
        <Text style={styles.paragraph}>{title}</Text>
      </Pressable>
    </View>
    )
}


